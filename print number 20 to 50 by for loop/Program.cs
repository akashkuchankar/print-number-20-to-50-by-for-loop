using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace print_number_20_to_50_by_for_loop
{
    internal class Program
    {
        static void Main(string[] args)
        {
            for(int i =20; i<=50; i++)
            {
                Console.WriteLine(i);
            }
            Console.ReadLine();
        }
    }
}
